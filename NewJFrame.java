    public static void Game(){
        int computer = rand.nextInt(3) + 1;
            if (computer == 1){
                
                jLabel2.setText("Computer Pick: Rock");
            }
            else if(computer == 2){

                jLabel2.setText("Computer Pick: Paper");
            }
            else if(computer == 3) {

                jLabel2.setText("Computer Pick: Scissors");
            }

            if (player_choice == computer){
                jLabel4.setText("Draw");
            }
            else if (player_choice == 1 && computer == 2){
                jLabel4.setText("Computer Wins");
            }
            else if (player_choice == 1 && computer==3){
                jLabel4.setText("Player Wins");
            }
            else if (player_choice ==2 && computer==3){
                jLabel4.setText("Computer Wins");
            }
            else if (player_choice ==2 && computer==1){
                jLabel4.setText("Player Wins");
            }
            else if (player_choice ==3 && computer==2){
                jLabel4.setText("Player Wins");
            }
            else if (player_choice ==3 && computer==1){
                jLabel4.setText("Computer Wins");
    }
    }

